public class Bitwise_Operators {
    public static void main(String[] args) {
        /*
         & -> Bitwise and operator  1100 & 0011 -> 0000
         | -> Bitwise OR operator   1100 & 0011 -> 1111
         << -> Left Shift Operator  1100 << 1 -> 1000 
         >> -> Right Shift Operator 1100 >> 1 -> 0110
         ^ -> Bitwise XOR Operator  1010 ^ 0111 -> 1101 (False when both are 1 or 0)
         */

         int test = 10;
         System.out.print(test>>1); 
        }

        // 10100
        // 0100
}
